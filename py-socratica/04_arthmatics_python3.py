anInt = 3
aFloat = 2.1
aComplex = 4.0 + 0j

#addition
print(anInt + aFloat)
type(anInt + aFloat)

#subtract
print(anInt - aFloat)
type(anInt - aFloat)
print(anInt - aComplex)
type(anInt - aComplex)

#multiplication
print(anInt * aFloat)
type(anInt * aFloat)

#division
print(anInt / aFloat)
type(anInt / aFloat)
print(anInt / aComplex)
type(anInt / aComplex) #in py2 return real portion of complex number

print(anInt % aFloat)
type(anInt % aFloat)

